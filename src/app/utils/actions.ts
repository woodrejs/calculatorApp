export const add = (num1: number, num2: number) => {
  return String(num1 + num2);
};
export const sub = (num1: number, num2: number) => {
  return String(num1 - num2);
};
export const div = (num1: number, num2: number) => {
  return String(num1 / num2);
};
export const multi = (num1: number, num2: number) => {
  return String(num1 * num2);
};
export const pow = (num: number) => {
  return String(Math.pow(num, 2));
};
export const sqrt = (num: number) => {
  return String(Math.sqrt(num));
};
